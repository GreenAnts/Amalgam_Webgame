// Build board dictionary for quick lookup
export function createBoardDict() {
    const boardDict = {};
    GOLDEN_COORDS.forEach(coord => {
        boardDict[coord] = "golden";
    });
    STANDARD_COORDS.forEach(coord => {
        if (!boardDict[coord]) {
            boardDict[coord] = "standard";
        }
    });
    return boardDict;
}

// Create golden connections set for rendering
export function createGoldenConnectionsSet() {
    const goldenConnections = new Set();
    for (const coordStr in GOLDEN_LINES_DICT) {
        const connections = GOLDEN_LINES_DICT[coordStr];
        const parts = coordStr.split(',').map(Number);
        const x1 = parts[0];
        const y1 = parts[1];
        if (Array.isArray(connections)) {
            connections.forEach(target => {
                const x2 = target.x;
                const y2 = target.y;
                const key = `${Math.min(x1, x2)},${Math.min(y1, y2)}-${Math.max(x1, x2)},${Math.max(y1, y2)}`;
                goldenConnections.add(key);
            });
        }
    }
    return goldenConnections;
}
